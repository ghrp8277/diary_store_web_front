<template>
  <div class="success-content">
    <div class="confirm-step">
      <ul class="progressbar">
        <li class="active">제출완료</li>
        <li>심사중</li>
        <li>심사완료</li>
        <li>상품화</li>
      </ul>
    </div>

    <div class="success-comment">
      <p><b>감사합니다.</b></p>
      <p><b>제안이 정상적으로 제출 되었습니다.</b></p>
    </div>

    <span class="success-sub-comment"
      >이모티콘 심사 결과는 1개월 이내에 이메일 / 제안 관리에서 확인
      가능합니다.</span
    >

    <table>
      <thead>
        <tr>
          <th>제안상태</th>
          <th>이모티콘 상품명</th>
          <th>이모티콘 카테고리</th>
          <th>태그</th>
        </tr>
      </thead>
      <tbody>
        <tr>
          <td>{{ confirmMatched }}</td>
          <td>{{ product_name }}</td>
          <td>{{ category }}</td>
          <td>{{ tag }}</td>
        </tr>
      </tbody>
    </table>
    <router-link :to="{ name: 'proposals' }" tag="button" class="submit">
      <span>제안 관리</span>
    </router-link>
  </div>
</template>

<script lang="ts">
import { IsConfirm } from '@/types/emojiConfirm';
import { defineComponent, toRefs } from '@vue/composition-api';

export default defineComponent({
  name: 'SuccessPageView',
  props: {
    product_name: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    tag: {
      type: String,
      required: true,
    },
    is_confirm: {
      type: Number,
      required: true,
    },
  },
  setup(props) {
    const { is_confirm } = toRefs(props);

    function confirmMatched() {
      const index = IsConfirm[is_confirm.value];

      let result = '';

      switch (index) {
        case 'SUBMISSION_COMPLETE':
          result = '제출완료';
          break;
        case 'UNDER_REVIEW':
          result = '심사중';
          break;
        case 'NOT_APPROVED':
          result = '미승인';
          break;
        case 'APPROVED':
          result = '승인';
          break;
      }

      return result;
    }

    return {
      confirmMatched: confirmMatched(),
    };
  },
});
</script>

